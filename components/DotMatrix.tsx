'use client';

import { useEffect, useState } from 'react';

interface DotMatrixProps {
  frames: string[][];
  intervalMs?: number;
  dotSize?: number;
  gap?: number;
  color?: string;
  dimColor?: string;
}

const SCALE: Record<string, number> = {
  '.': 0,
  ' ': 0,
  '·': 0.25,
  ':': 0.45,
  '+': 0.6,
  '*': 0.75,
  'o': 0.85,
  '#': 1,
  '@': 1,
};

export default function DotMatrix({
  frames,
  intervalMs = 220,
  dotSize = 4,
  gap = 4,
  color = '#fff',
  dimColor = '#222',
}: DotMatrixProps) {
  const [idx, setIdx] = useState(0);

  useEffect(() => {
    if (frames.length <= 1) return;
    const t = setInterval(() => setIdx(i => (i + 1) % frames.length), intervalMs);
    return () => clearInterval(t);
  }, [frames.length, intervalMs]);

  const frame = frames[idx] ?? frames[0];
  const cols = Math.max(...frame.map(r => r.length));

  return (
    <div
      style={{
        display: 'grid',
        gridTemplateColumns: `repeat(${cols}, ${dotSize}px)`,
        gridTemplateRows: `repeat(${frame.length}, ${dotSize}px)`,
        gap,
        justifyContent: 'center',
        alignContent: 'center',
      }}
    >
      {frame.flatMap((row, ri) =>
        Array.from({ length: cols }).map((_, ci) => {
          const ch = row[ci] ?? '.';
          const intensity = SCALE[ch] ?? 0;
          return (
            <div
              key={`${ri}-${ci}`}
              style={{
                width: dotSize,
                height: dotSize,
                borderRadius: '50%',
                background: intensity === 0 ? dimColor : color,
                opacity: intensity === 0 ? 0.18 : 0.4 + intensity * 0.6,
                transition: 'opacity 0.18s ease, background 0.18s ease',
              }}
            />
          );
        })
      )}
    </div>
  );
}

